'''Crea un programa que realice operaciones aritméticas simples (suma, resta,
multiplicación, división) según la elección del usuario.'''

def sumar (x,y):
  return x + y
resultado =sumar(8,2)
print(resultado)

def restar (x,y):
  return x-y
resultado=restar(8,2)
print (resultado)

def multiplicar (x,y):
  return x*y
resultado=multiplicar(8,2)
print (resultado)

def dividir (x,y):
  return x/y
resultado=dividir(8,2)
print (resultado)